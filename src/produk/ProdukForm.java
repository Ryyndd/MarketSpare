    private void buka_tabel() {
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("NO");
        model.addColumn("SUPLIER");
        model.addColumn("NAMA");
        model.addColumn("KATEGORI");
        model.addColumn("HARGA");
        model.addColumn("STOK");
        

        // menampilkan data kedalam jtable
        try {
            String sql = "select*from tb_barang";
           
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{res.getInt(1), res.getString(2),
                    res.getString(3),res.getString(4),res.getInt(5), res.getInt(6)});
            }
            tabelBarang.setModel(model);
        } catch (Exception e) {
        }
    }

    // Validation methods
    private boolean isValidInt(String input) {
        if (input == null || input.isEmpty()) {
            return false;
        }
        try {
            Integer.parseInt(input);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    private boolean isValidString(String input) {
        return input != null && !input.trim().isEmpty();
    }

    
