    private void comboBox() {
        try {
           // Fungsi Untuk mengambil nilai enum pada database 
            String sql = "SHOW COLUMNS FROM tb_barang LIKE 'kategori_barang'";
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            java.sql.ResultSet cek = pst.executeQuery();

            if (cek.next()) {
                String enumValue = cek.getString("Type");
                enumValue = enumValue.replace("enum(", "")
                        .replace(")", "").replace("'", "");

                String[] values = enumValue.split(",");

                DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

                for (String value : values) {
                    model.addElement(value.trim());
                }
                kategoriBarang.setModel(model);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void namaSuplierComboBox() {
        try {
           // Implemntasi Relasi Database yang ditampilkan pada form PRoduk 
            String sql = "SELECT nama_supplier FROM tb_supplier ";
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String namaSupplier = rs.getString("nama_supplier");
                supplierComboBox.addItem(namaSupplier);
            }

            rs.close();
            pst.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal mengambil data suplier: " + e.getMessage());
        }
    }


