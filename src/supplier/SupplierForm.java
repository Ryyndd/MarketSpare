    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        // Validasi Input
        if (!isValidString(kodeSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Kode Supplier tidak boleh kosong.");
            return;
        }
        if (!isValidString(namaSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Nama Supplier tidak boleh kosong.");
            return;
        }
        if (!isValidString(teleponSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Nomor Supplier harus berupa angka 0-9.");
            return;
        }
        if (!isValidString(alamatSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Alamat Supplier tidak boleh kosong.");
            return;
        }
        
        
        
        try {
            
            String sql = "INSERT INTO tb_supplier (kode_supplier, nama_supplier, telepon_supplier, alamat_supplier ) VALUES"
                    + "(?, ?, ?, ?)";
            
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, kodeSupplier.getText());
            pst.setString(2, namaSupplier.getText());
            pst.setString(3, teleponSupplier.getText());
            pst.setString(4, alamatSupplier.getText());
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data Supplier Berhasil Disimpan");
            kodeSupplier.setText("");
            namaSupplier.setText("");
            teleponSupplier.setText("");
            alamatSupplier.setText("");

            // Set focus
            kodeSupplier.requestFocusInWindow();

            buka_tabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus ' " + namaSupplier.getText() + "'", " Konfirmasi Hapus ",
                JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                
                String sql = "DELETE FROM tb_supplier WHERE kode_supplier= '" + kodeSupplier.getText().trim() + " ' " ;
                java.sql.PreparedStatement pst = con.prepareStatement(sql);
                pst.execute();

                JOptionPane.showMessageDialog(null, "Data Supplier Berhasil Dihapus");
                kodeSupplier.setText("");
                namaSupplier.setText("");
                teleponSupplier.setText("");
                alamatSupplier.setText("");
                kodeSupplier.enable();
                kodeSupplier.requestFocusInWindow();
                buka_tabel();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,
                        "Proses hapus gagal/koneksi gagal .. ");
                System.out.println(e.getMessage());

            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

