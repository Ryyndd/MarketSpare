    private void buka_tabel() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("KODE");
        model.addColumn("NAMA");
        model.addColumn("TELEPON");
        model.addColumn("ALAMAT");

        // menampilkan data kedalam table
        try {
            String sql = "SELECT kode_supplier, nama_supplier, telepon_supplier, alamat_supplier FROM tb_supplier";
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("kode_supplier"),
                    res.getString("nama_supplier"),
                    res.getString("telepon_supplier"),
                    res.getString("alamat_supplier")
                });
            }
            tabelSupplier.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


