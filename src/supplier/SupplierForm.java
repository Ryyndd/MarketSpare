    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        
        if (!isValidString(namaSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Nama Supplier tidak boleh kosong.");
            return;
        }
        if (!isValidString(teleponSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Nomor Supplier harus berupa angka 0-9.");
            return;
        }
        
        if (!isValidString(alamatSupplier.getText())) {
            JOptionPane.showMessageDialog(null, "Alamat Supplier tidak boleh kosong.");
            return;
        }
        try {
            
            String sql = "UPDATE tb_supplier SET "
                    + "nama_supplier=" + "'" + namaSupplier.getText() + "',"
                    + "telepon_supplier=" + "'" + teleponSupplier.getText() + "',"
                    + "alamat_supplier=" + "'" + alamatSupplier.getText() + "'"
                    + "WHERE kode_supplier=" + "'" + kodeSupplier.getText() + "';";
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Data berhasil di Edit");
            namaSupplier.setText("");
            teleponSupplier.setText("");
            alamatSupplier.setText("");
            namaSupplier.setEnabled(true);
            namaSupplier.requestFocusInWindow();
            buka_tabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses edit gagal .. ");
            System.out.println(e.getMessage());

        }
    }//GEN-LAST:event_btnEditActionPerformed

